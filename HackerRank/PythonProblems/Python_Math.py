#BUILT IN MATH
x = min(5, 10, 25)
y = max(5, 10, 25)
print(x)
print(y)

x = abs(-7.25)
print(x)

x = pow(4, 3)   #4^3 SAME AS 4**3
print(x)

#IMPORTED MATH
import math

x = math.sqrt(64)
print(x)

x = math.ceil(1.4)
y = math.floor(1.4)
print(x)    # returns 2
print(y)    # returns 1

x = math.pi
print(x)    #constant pi value